void test_isprime_ulong() {
    bool pass1 = true;
    auto start = std::chrono::high_resolution_clock::now();
    for (int i = 0; i < 140; i++) {
        if (lmmp_is_prime_ulong_(test[i])) {
            std::cout << "prime: " << test[i] << std::endl;
            pass1 = false;
        }
    }
    auto end = std::chrono::high_resolution_clock::now();
    auto duration = std::chrono::duration_cast<std::chrono::microseconds>(end - start).count();
    std::cout << "strong pseudoprimes took " << duration << " microseconds." << std::endl;
    if (pass1) {
        std::cout << "pseudoprimes are tested successfully." << std::endl;
    } else {
        std::cout << "pseudoprimes are not tested successfully." << std::endl;
    }
    std::cout << "\n";

    bool pass2 = true;
    start = std::chrono::high_resolution_clock::now();
    for (int i = 0; i < 200; i++) {
        if (!lmmp_is_prime_ulong_(prime_numbers[i])) {
            std::cout << "not prime: " << prime_numbers[i] << std::endl;
            pass2 = false;
        }
    }
    end = std::chrono::high_resolution_clock::now();
    duration = std::chrono::duration_cast<std::chrono::microseconds>(end - start).count();
    std::cout << "real primes took " << duration << " microseconds." << std::endl;
    if (pass2) {
        std::cout << "all prime numbers are tested successfully." << std::endl;
    } else {
        std::cout << "some prime numbers are not tested successfully." << std::endl;
    }
}